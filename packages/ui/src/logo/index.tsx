interface ILogoProps {
  variant: "appIcon" | "text" | "icon";
  appIconProps?: IAppIconProps;
  textProps?: ITextProps;
  iconProps?: IIconProps;
}
export function Logo({
  variant,
  appIconProps,
  textProps,
  iconProps,
}: ILogoProps) {
  if (variant === "icon" && iconProps) {
    return <Icon {...iconProps} />;
  }

  if (variant === "text" && textProps) {
    return <Text {...textProps} />;
  }

  if (variant === "appIcon" && appIconProps) {
    return <AppIcon {...appIconProps} />;
  }
  return <></>;
}

